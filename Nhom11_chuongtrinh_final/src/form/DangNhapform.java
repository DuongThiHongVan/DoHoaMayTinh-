    public boolean dangnhap(TaiKhoan tk) {
        try {
        String taikhoan = txt_taikhoandn.getText();
        String matkhau = txt_matkhaudn.getText();
        tk = new TaiKhoan(taikhoan, matkhau);

        for (TaiKhoan taiKhoan : list) {
            if (taiKhoan.getTaikhoan().equals(taikhoan) && taiKhoan.getMatkhau().equals(matkhau)) {
                return true;
            }
        }
    } catch (Exception e) {
        // Xử lý lỗi tại đây
        JOptionPane.showMessageDialog(rootPane, "LỖI THÔNG TIN", "THÔNG BÁO", HEIGHT);
        e.printStackTrace();
    }
    return false;
    }
    

