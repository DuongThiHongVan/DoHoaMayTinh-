    private void btn_dangnhapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_dangnhapActionPerformed
        // TODO add your handling code here:

        if (check_xacnhan.isSelected()) {
    try {
        TaiKhoan tk = new TaiKhoan();
        String taikhoan = txt_taikhoandn.getText();
        String matkhau = txt_matkhaudn.getText();
        if (dangnhap(tk)) {
            JOptionPane.showMessageDialog(rootPane, "Đăng nhập thành công", "Welcome!!!", HEIGHT);
            QLTTthanhvienform form = new QLTTthanhvienform();
            form.setVisible(true);
            form.setLocationRelativeTo(null);
        } else {
            if (taikhoan.equals("") || matkhau.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Vui lòng điền đầy đủ thông tin", "THÔNG BÁO", HEIGHT);
                check_xacnhan.setSelected(false);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Tài khoản hoặc mật khẩu của bạn không đúng!", "THÔNG BÁO️", HEIGHT);
                txt_matkhaudn.setText("");
                txt_taikhoandn.setText("");
                check_xacnhan.setSelected(false);
            }
        }
    } catch (Exception e) {
        // Xử lý lỗi tại đây
        JOptionPane.showMessageDialog(rootPane, "LỖI THÔNG TIN", "THÔNG BÁO", HEIGHT);
        e.printStackTrace();
    }
} else {
    JOptionPane.showMessageDialog(rootPane, "Bạn chưa xác nhận", "THÔNG BÁO", HEIGHT);
    check_xacnhan.setSelected(false);
}
    }//GEN-LAST:event_btn_dangnhapActionPerformed

