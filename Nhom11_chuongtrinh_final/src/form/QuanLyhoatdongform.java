    private void btn_xoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_xoaActionPerformed
        // TODO add your handling code here:
        String mhd = txt_mahd.getText();
        HoatDong hd = hdm.findhd(mhd);
        
        if (kiemtratontai(mhd)<0) {
            hdm.xoa(mhd);
            xoatvtrongds(hd);
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập mã hd", "Thông báo", HEIGHT);
            resetForm();
        } else {
            if (mhd.isEmpty()) {
                JOptionPane.showMessageDialog(rootPane, "Xóa thành công", "Thông báo", HEIGHT);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Hoạt động ko tồn tại", "Thông báo", HEIGHT);
         
  resetForm();
            }
        }
    }//GEN-LAST:event_btn_xoaActionPerformed

     

       
        
                                        
 public void xoatvtrongds(HoatDong hd) {
        int rowcount = tb_danhsach.getRowCount();
        int indexrow = -1;
        String mhd = hd.getMahd();
        for (int i = 0; i < rowcount; i++) {
            //tim msv can xoa trong table
            String value = tb_model.getValueAt(i, 0).toString();
            if (value.equals(mhd)) {
                indexrow = i;
                break;
            }
        }
        if (indexrow >= 0) {
            tb_model.removeRow(indexrow);

        }
    }
    public int kiemtratontai(String mhd) {
        mhd = txt_mahd.getText();
        for (HoatDong hoatDong : list) {
            if (hoatDong.getMahd().equals(mhd)) {
                return -1;
            }
        }
        return 1;
    }
