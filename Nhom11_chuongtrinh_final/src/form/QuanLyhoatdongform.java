    public void setModel(HoatDong hd) {
        hd.setMahd(txt_mahd.getText());
        hd.setTenhd(txt_tenhd.getText());
        hd.setDiadiem(txt_diadiem.getText());
        try {
            hd.setKinhphi(Long.parseLong(txt_kinhphi.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Kinh phí phải là số", "Thông báo", HEIGHT);
        }
        try {
            hd.setSoluong(Integer.parseInt(txt_sl.getText()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Số lưởng phải là kiểu số nguyên", "Thông báo", HEIGHT);
        }
    }

